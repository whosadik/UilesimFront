// Generate more products for the feed
export const generateProducts = (count: number) => {
  const products = [];
  for (let i = 0; i < count; i++) {
    const baseProduct = mockProducts[i % mockProducts.length];
    products.push({
      ...baseProduct,
      id: 100 + i,
      title: `${baseProduct.title} ${i + 1}`,
    });
  }
  return products;
};

// Convert mock products to ProductGrid format
export const convertToProductGridFormat = (productsToConvert: typeof mockProducts) => {
  return productsToConvert.map((product) => ({
    id: String(product.id),
    name: product.title,
    brand: product.brand,
    price: product.price,
    originalPrice: product.oldPrice,
    image: product.image,
    category: 'skincare', // default category
    isNew: product.isNew,
    discount: product.discount,
    rating: product.rating,
    inStock: true,
    pointsEarned: Math.floor(product.price * 0.01), // 1% points
    recommendationScore: product.rating ? Math.floor(product.rating * 20) : undefined,
  }));
};

// Converted products for use in ProductGrid
export const products = convertToProductGridFormat(mockProducts);
